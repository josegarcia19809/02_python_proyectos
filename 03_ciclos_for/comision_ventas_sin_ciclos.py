# Obtener las ventas y la tasa de comisión de un vendedor.
ventas = float(input('Ingrese el monto de ventas: '))
tasa_comision = float(input('Ingrese la tasa de comisión: '))

# Calcular la comisión.
comision = ventas * tasa_comision

# Mostrar la comisión.
print('La comisión es $', format(comision, ',.2f'), sep='')

