# Se quiere mostrar los datos de un estudiante, cada dato se mostrará en una función diferente
# nombre: mostrar_nombre()
# carrera: visualizar_carrera()
# promedio: imprimir_promedio()

def mostrar_nombre():
    print("Nombre: José L. García")


def visualizar_carrera():
    print("Carrera: Ingeniería en Computación")


def imprimir_promedio():
    print("Promedio: 8.8")


def main():
    mostrar_nombre()
    visualizar_carrera()
    imprimir_promedio()


main()
