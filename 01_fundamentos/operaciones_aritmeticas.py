# Este programa ilustra el uso de print para desplegar los resultados de operaciones
# aritméticas dentro de las instrucciones de un programa
print("OPERACIONES BÁSICAS", "-" * 30)
print(f"15 mas 2 es igual a {15 + 2}")
print(f"15 menos 2 es igual a {15 - 2}")
print(f"15 por 2 es igual a {15 * 2}")
print(f"15 entre 2 es igual a {15 / 2}")
print()

# División entera
print("DIVISIÓN ENTERA (SIN DECIMALES)", "-" * 17)
print(f"15 entre 2 es igual a {15 // 2}")
print(f"20 entre 3 es igual a {20 // 3}")
print(f"-20 entre 3 es igual a {-20 // 3}")
print()

# Residuo o módulo
print("RESIDUO DE UNA DIVISIÓN ENTERA (MÓDULO)", "-" * 9)
print(f"15 módulo 2 es igual a {15 % 2}")
print(f"20 módulo 3 es igual a {20 % 3}")
print(f"15 módulo 3 es igual a {15 % 3}")
print(f"50 módulo 7 es igual a {50 % 7}")
print(f"100 módulo 8 es igual a {100 % 8}")
print()

